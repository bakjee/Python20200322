
# while 문을 사용하여 합계를 구하시오
# 무한 반복과 반복문(루프) 탈출을 결합한 예제
# 페이지 134 참고
print("종료하시려면 음수를 입력하세용")
합계 = 0
횟수 = 0
while True:
    str1 = int(input("성적을 입력하삼"))

# stgr1이 음수이면 루플를 탈출하라
    if str1 < 0:
        break
    else:
        합계 = 합계+str1
        횟수 = 횟수+1
평균 = 합계/횟수
str1 = "성적의 평균은 %s  점" % 평균
print(str1)

# 무한 반복문은 조건식을 True 로 하면 된다.
# 루프 탈출은 break 를 사용하면 된다.
